#include "syscall.h"
#include "traps.h"

# macros must be defined on a single line, hence we use backslash
# double hash is the token pasting operator
#define SYSCALL(name) \
  .globl name; \
  name: \
    movl $SYS_ ## name, %eax; \
    int $T_SYSCALL; \
    ret

SYSCALL(fork)
SYSCALL(exit)
